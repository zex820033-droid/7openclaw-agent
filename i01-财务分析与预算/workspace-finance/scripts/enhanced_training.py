def fmt(n):
    if n is None: return "—"
    return f"{n:,.0f}"

def gen_period(offset=0, volatility=1.0):
    """生成一期财务数据，volatility控制波动幅度"""
    rev = 3_800_000 + random.randint(-500_000, 600_000) * volatility
    cogs = round(rev * random.uniform(0.55, 0.72))
    gp = rev - cogs
    opex = round(rev * random.uniform(0.18, 0.32))
    dep = round(12_000_000 * 0.02 / 12)
    op = gp - opex - dep
    interest = round(5_000_000 * random.uniform(0.04, 0.07) / 12)
    pbt = op - interest
    tax = round(max(0, pbt) * 0.25)
    npv = pbt - tax
    cash = 9_000_000 + npv * 3 + random.randint(-500_000, 500_000)
    ar = round(rev * random.uniform(0.5, 1.5))
    inv = round(cogs * random.uniform(0.2, 0.8))
    ap = round(cogs * random.uniform(0.3, 0.9))
    return {"period": f"2026-{(6+offset):02d}", "rev": rev, "cogs": cogs, "gp": gp,
            "opex": opex, "dep": dep, "op": op, "interest": interest, "pbt": pbt,
            "tax": tax, "npv": npv, "cash": cash, "ar": ar, "inv": inv, "ap": ap}

